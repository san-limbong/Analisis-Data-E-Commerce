# Helper function
def create_sum_order_items_df(df):
    sum_order_items_df = df.groupby("product_category_name_english").order_id.nunique().sort_values(ascending=False).reset_index()
    sum_order_items_df.rename(columns={
    "product_category_name_english": "product_category_name",
    "order_id" : "product_count"}, inplace=True)
    
    return sum_order_items_df

def create_bycity_df(df):
    bycity_df = df.groupby(by="customer_city").order_id.nunique().sort_values(ascending=False).reset_index()
    bycity_df.rename(columns={"order_id": "order_count"}, inplace=True)
    
    return bycity_df

def create_rfm_df(df):
    customer_id_mapping = {customer_id: i +1 for i, customer_id in enumerate(all_df['customer_id'].unique())}
    all_df['id'] = all_df['customer_id'].map(customer_id_mapping)
    rfm_df = df.groupby(by="id", as_index=False).agg({
        "order_purchase_timestamp": "max", #mengambil tanggal order terakhir
        "order_id": "nunique",
        "price": "sum"
    })
    rfm_df.columns = ["customer_id", "max_order_timestamp", "frequency", "monetary"]
    rfm_df["max_order_timestamp"] = rfm_df["max_order_timestamp"].dt.date
    recent_date = all_df["order_purchase_timestamp"].dt.date.max()
    rfm_df["recency"] = rfm_df["max_order_timestamp"].apply(lambda x: (recent_date - x).days)
    rfm_df.drop("max_order_timestamp", axis=1, inplace=True)
    return rfm_df

